def write_if_changed(path: Path, content: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and path.read_text(encoding="utf-8") == content:
        return
    path.write_text(content, encoding="utf-8")


def build_records() -> list[DocumentRecord]:
    groups = discover_source_groups()
    records: list[DocumentRecord] = []
    for key, paths in sorted(groups.items()):
        variants = [extract_variant(path) for path in sorted(paths)]
        primary = pick_primary_variant(variants)
        manual = MANUAL_METADATA.get(key, {})
        title = manual.get("title") or primary.extracted_title or prettify_stem(key)
        aliases = manual.get("aliases") or ([primary.extracted_title] if primary.extracted_title and primary.extracted_title != title else [])
        artifact_type = manual.get("artifact_type") or derive_artifact_type(key)
        collection = manual.get("collection") or derive_collection(artifact_type)
        domain = manual.get("domain") or derive_domain(key)
        summary = manual.get("summary") or summarize_preview(primary.preview)
        status = manual.get("status") or extract_status(primary.preview)
        filename = slugify(title) + ".md"
        relative_note_path = Path("Knowledge Base") / "Documents" / collection / filename
        note_path = VAULT_ROOT / relative_note_path
        note_link = build_note_link(relative_note_path, title)
        related_keys = RELATED_DOCUMENTS.get(key, [])
        records.append(
            DocumentRecord(
                key=key,
                title=title,
                aliases=aliases,
                artifact_type=artifact_type,
                collection=collection,
                domain=domain,
                summary=summary,
                status=status,
                note_path=note_path,
                note_link=note_link,
                variants=variants,
                related_keys=related_keys,
            )
        )
    return records


def main() -> None:
    if not VAULT_ROOT.exists():
        raise SystemExit(f"Vault root does not exist: {VAULT_ROOT}")

    records = build_records()
    lookup = {record.key: record for record in records}

    for record in records:
        write_if_changed(record.note_path, build_document_note(record, lookup))

    map_root = KB_ROOT / "Maps"
    for name in COLLECTION_MAP_NOTES:
        write_if_changed(map_root / f"{name}.md", build_map_note(name, sort_records(records), lookup))

    indexes_root = KB_ROOT / "Indexes"
    write_if_changed(indexes_root / "Document Library.md", build_document_library(records))
    write_if_changed(indexes_root / "Source Inventory.md", build_source_inventory(records))
    write_if_changed(VAULT_ROOT / "Home.md", build_home_note(records))

    generated_at = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
    print(f"Synced Obsidian knowledge base at {generated_at}")
    print(f"Canonical documents: {len(records)}")
    print(f"Source files tracked: {sum(len(record.variants) for record in records)}")


if __name__ == "__main__":
    main()
