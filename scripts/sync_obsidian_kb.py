def derive_domain(stem: str) -> str:
    lower = stem.lower()
    if "construction" in lower or "procore" in lower or "sharepoint" in lower or "teams" in lower:
        return "construction"
    if "buildops" in lower or "github" in lower or "linear" in lower or "sentry" in lower:
        return "ai-buildops"
    if "rpx" in lower:
        return "rpx"
    return "platform"


def extract_status(preview: str) -> str | None:
    patterns = [
        r"Status[: ]+([A-Za-z0-9.\- ]{4,60})",
        r"Document status[: ]+([A-Za-z0-9.\- |]{4,60})",
        r"VERSION[: ]+([A-Za-z0-9.\- ]{3,30})",
    ]
    for pattern in patterns:
        match = re.search(pattern, preview, flags=re.IGNORECASE)
        if match:
            return clean_text(match.group(1))
    return None


def summarize_preview(preview: str) -> str:
    sentences = re.split(r"(?<=[.!?])\s+", clean_text(preview))
    selected: list[str] = []
    for sentence in sentences:
        sentence = sentence.strip()
        if not sentence:
            continue
        selected.append(sentence)
        if len(" ".join(selected)) >= 280 or len(selected) >= 2:
            break
    if not selected:
        return "Generated from the source file inventory."
    return truncate(" ".join(selected), 320)


def discover_source_groups() -> dict[str, list[Path]]:
    groups: dict[str, list[Path]] = {}
    for path in sorted(SOURCE_ROOT.rglob("*")):
        if not path.is_file():
            continue
        if path.name.startswith("."):
            continue
        if path.suffix.lower() not in SUPPORTED_EXTENSIONS:
            continue
        groups.setdefault(path.stem, []).append(path)
    return groups


def extract_docx_variant(path: Path) -> SourceVariant:
    try:
        result = subprocess.run(
            ["textutil", "-convert", "txt", "-stdout", str(path)],
            check=True,
            capture_output=True,
            text=True,
        )
        preview = clean_text(result.stdout)
    except subprocess.CalledProcessError:
        preview = ""
    title = preview.splitlines()[0].strip() if preview else None
    return SourceVariant(path=path, extension=".docx", preview=truncate(preview, 1600), extracted_title=title)


def extract_pdf_variant(path: Path) -> SourceVariant:
    preview_chunks: list[str] = []
    page_count: int | None = None
    extracted_title: str | None = None
    try:
        reader = PdfReader(str(path))
        page_count = len(reader.pages)
        if getattr(reader, "metadata", None):
            extracted_title = getattr(reader.metadata, "title", None)
        for page in reader.pages[:2]:
            page_text = clean_text(page.extract_text() or "")
            if page_text:
                preview_chunks.append(page_text)
            if len(" ".join(preview_chunks)) >= 1800:
                break
    except Exception:
        pass
    preview = truncate(" ".join(preview_chunks), 1600)
    return SourceVariant(
        path=path,
        extension=".pdf",
        preview=preview,
        extracted_title=extracted_title,
        page_count=page_count,
    )


def extract_yaml_variant(path: Path) -> SourceVariant:
    raw = path.read_text(encoding="utf-8")
    data = yaml.safe_load(raw) or {}
    preview = clean_text(raw)
    title = None
    metadata = data.get("metadata", {})
    if isinstance(metadata, dict):
        title = metadata.get("displayName") or metadata.get("overlayId")
    return SourceVariant(
        path=path,
        extension=path.suffix.lower(),
        preview=truncate(preview, 1600),
        extracted_title=title,
        yaml_data=data if isinstance(data, dict) else None,
    )


def extract_variant(path: Path) -> SourceVariant:
    extension = path.suffix.lower()
    if extension == ".docx":
        return extract_docx_variant(path)
    if extension == ".pdf":
        return extract_pdf_variant(path)
    return extract_yaml_variant(path)


def pick_primary_variant(variants: list[SourceVariant]) -> SourceVariant:
    priority = {".docx": 0, ".pdf": 1, ".yaml": 2, ".yml": 2}
    return sorted(variants, key=lambda variant: priority.get(variant.extension, 10))[0]


def build_note_link(relative_path: Path, title: str) -> str:
    target = relative_path.with_suffix("").as_posix()
    return f"[[{target}|{title}]]"


def yaml_snapshot_lines(data: dict[str, Any] | None) -> list[str]:
    if not data:
        return []
    metadata = data.get("metadata", {}) if isinstance(data, dict) else {}
    connectors = data.get("connectors", {}) if isinstance(data, dict) else {}
    workflows = data.get("workflows", {}) if isinstance(data, dict) else {}
    trust_boundary = data.get("trustBoundary", {}) if isinstance(data, dict) else {}
    lines: list[str] = []
    if isinstance(metadata, dict):
        if metadata.get("overlayId"):
            lines.append(f"- Overlay ID: `{metadata['overlayId']}`")
        if metadata.get("packRef"):
            lines.append(f"- Pack reference: `{metadata['packRef']}`")
        if metadata.get("owner"):
            lines.append(f"- Owner: `{metadata['owner']}`")
        if metadata.get("displayName"):
            lines.append(f"- Display name: {metadata['displayName']}")
    if isinstance(trust_boundary, dict) and trust_boundary.get("key"):
        lines.append(f"- Trust boundary: `{trust_boundary['key']}`")
    if isinstance(connectors, dict) and connectors:
        connector_names = ", ".join(f"`{name}`" for name in sorted(connectors))
        lines.append(f"- Connectors: {connector_names}")
    if isinstance(workflows, dict) and workflows:
        workflow_names = ", ".join(f"`{name}`" for name in sorted(workflows))
        lines.append(f"- Workflows: {workflow_names}")
    return lines


def build_frontmatter(record: DocumentRecord, relative_sources: list[str]) -> str:
    frontmatter = {
        "aliases": record.aliases,
        "type": "kb/document",
        "collection": record.collection.lower().replace(" ", "-"),
        "artifact_type": record.artifact_type,
        "domain": record.domain,
        "generated": True,
        "source_files": relative_sources,
        "tags": [
            "kb/document",
            f"collection/{record.collection.lower().replace(' ', '-')}",
            f"artifact/{record.artifact_type}",
            f"domain/{record.domain}",
        ],
    }
    return "---\n" + yaml.safe_dump(frontmatter, sort_keys=False, allow_unicode=False).strip() + "\n---"


def build_document_note(record: DocumentRecord, note_lookup: dict[str, DocumentRecord]) -> str:
    relative_sources = [variant.path.relative_to(VAULT_ROOT).as_posix() for variant in record.variants]
    sections = [
        build_frontmatter(record, relative_sources),
        GENERATED_MARKER,
        f"# {record.title}",
        "",
        "## Summary",
        record.summary,
        "",
        "## Source Files",
    ]

    for variant in sorted(record.variants, key=lambda item: item.extension):
        relative = Path(os.path.relpath(variant.path, start=record.note_path.parent)).as_posix()
        details = [variant.extension.replace(".", "").upper()]
        if variant.page_count:
            details.append(f"{variant.page_count} pages")
        sections.append(f"- [{variant.path.name}]({relative}) ({', '.join(details)})")

    sections.extend(["", "## Snapshot"])
    sections.append(f"- Collection: {record.collection}")
    sections.append(f"- Artifact type: `{record.artifact_type}`")
    sections.append(f"- Domain: `{record.domain}`")
    if record.status:
        sections.append(f"- Status: {record.status}")
    sections.append(
        "- Formats: " + ", ".join(variant.extension.replace(".", "").upper() for variant in sorted(record.variants, key=lambda item: item.extension))
    )

    primary = pick_primary_variant(record.variants)
    if primary.yaml_data:
        yaml_lines = yaml_snapshot_lines(primary.yaml_data)
        if yaml_lines:
            sections.extend(["", "## Manifest Highlights", *yaml_lines])

    related_links = [
        note_lookup[key].note_link
        for key in record.related_keys
        if key in note_lookup and key != record.key
    ]
    if related_links:
        sections.extend(["", "## Related Notes"])
        sections.extend(f"- {link}" for link in related_links)

    if primary.preview:
        sections.extend(["", "## Extracted Preview", f"> {primary.preview.replace(chr(10), chr(10) + '> ')}"])

    sections.append("")
    return "\n".join(sections)


def sort_records(records: list[DocumentRecord]) -> list[DocumentRecord]:
    collection_order = {name: index for index, name in enumerate(COLLECTION_ORDER)}
    return sorted(records, key=lambda record: (collection_order.get(record.collection, 99), record.title.lower()))


